const uint8_t* get_icon_bitmap(enum icons icon)
{
    switch (icon)
    {
        case icon_brightness:
            return _icon_brightness;
        case icon_volume:
            return _icon_volume;
        case icon_battery:
            return _icon_battery;
        case icon_melody:
            return _icon_melody;
        case icon_snake:
            return _icon_snake;
        case icon_return:
            return _icon_return;
    }
    return nullptr;
}

const uint8_t* get_bitmap(char c)
{
    if (c >= '0' && c <= '9')
    {
        return font_table[c - '0'];
    }
    switch (c)
    {
        case 'h':
            return char_h;
    }
    return nullptr;
}
